public class 函数56J {
    public static void a(){
        System.out.println("a");
    }
    public static void a(int a){
        System.out.println(a);
    }
    public static void a(int a, int b){
        System.out.println(a+b);
    }
    public static void main(String[] args) {
        a();
        a(1);
        a(1,2);
    }
}
